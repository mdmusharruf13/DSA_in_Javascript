/**
 * A tree is a hierarchial data structure that consist of nodes connected by edges.
 * A tree is a non linear ds, conpared to arrays, linked list, stack and queue which are linear ds.
 * In linear ds, the time req. to search is proportional to the size of the data set.
 * Tree however, woing to the nonlinear nature allow quicker and easier to the data.
 * A tree will not contain any loop or cycles
 * 
 * Traversals:
 * Depth first search :
 * The DFS  algo starts at the root node and explores as far as possible along each branch before backtracking
 * Visit the root node, visit all the nodes in the left sub tree and visit all the nodes in the right sub tree.
 * Depending on the order in which we do this, there can be three types of DFS traversal:
 * 1. Preorder
 * 2. Inorder
 * 3. Postorder 
 */